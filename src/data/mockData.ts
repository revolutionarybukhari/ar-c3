// 7. Seasonal Data — 52 weeks with Ramadan and Eid spikes
// ---------------------------------------------------------------------------

export const seasonalData: SeasonalData[] = (() => {
  const data: SeasonalData[] = [];
  // Ramadan 2026: approx weeks 9-12 (early March)
  // Eid al-Fitr: approx week 13
  // Eid al-Adha: approx week 23 (early June)
  for (let w = 1; w <= 52; w++) {
    let baseDemand = 8000 + Math.sin((w / 52) * Math.PI * 2) * 1200;
    let baseSupply = 8500 + Math.sin(((w - 2) / 52) * Math.PI * 2) * 1000;
    let season: string | undefined;

    // Pre-Ramadan ramp-up (weeks 5-8)
    if (w >= 5 && w <= 8) {
      baseDemand *= 1.15 + (w - 5) * 0.05;
      season = "Pre-Ramadan";
    }
    // Ramadan (weeks 9-12)
    if (w >= 9 && w <= 12) {
      baseDemand *= 1.4;
      baseSupply *= 0.9;
      season = "Ramadan";
    }
    // Eid al-Fitr spike (weeks 13-14)
    if (w >= 13 && w <= 14) {
      baseDemand *= 1.8;
      season = "Eid al-Fitr";
    }
    // Pre-Eid al-Adha ramp (weeks 19-22)
    if (w >= 19 && w <= 22) {
      baseDemand *= 1.3 + (w - 19) * 0.1;
      season = "Pre-Eid al-Adha";
    }
    // Eid al-Adha peak (weeks 23-24)
    if (w >= 23 && w <= 24) {
      baseDemand *= 2.2;
      baseSupply *= 0.85;
      season = "Eid al-Adha";
    }
    // Post-Eid normalization (week 25)
    if (w === 25) {
      baseDemand *= 0.7;
      season = "Post-Eid";
    }
    // Year-end holiday uptick (weeks 50-52)
    if (w >= 50) {
      baseDemand *= 1.15;
      season = "Year-End";
    }

    const demand = Math.round(baseDemand);
    const supply = Math.round(baseSupply);
    const inventoryBase = 25000 + (supply - demand) * 2;
    const inventory = Math.max(5000, Math.round(inventoryBase + Math.sin((w / 52) * Math.PI) * 3000));

    data.push({ week: w, demand, supply, inventory, ...(season ? { season } : {}) });
  }
  return data;
})();

